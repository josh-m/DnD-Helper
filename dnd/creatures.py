class Creature():
    def __init__(self, c_type):
        self.name = c_type.name
        self.hit_die = [0,0]
        self.hp = 0
        
    
        if type(c_type) == DragonType:
            r = roll(8)
            if r < 3:
                dragon_size = 'Small'
                self.hit_die = [c_type.hit_die[0],0]
            elif r < 8:
                dragon_size = 'Average'
                self.hit_die = [c_type.hit_die[0] + 1,0]
            else:
                dragon_size = 'Huge'
                self.hit_die = [c_type.hit_die[0] + 2,0]
            
            r = roll(8)
            if r == 1: age = 'Very Young'
            elif r == 2: age = 'Young'
            elif r == 3: age = 'Sub-Adult'
            elif r == 4: age = 'Young Adult'
            elif r == 5: age = 'Adult'
            elif r == 6: age = 'Old'
            elif r == 7: age = 'Very Old'
            elif r == 8: age = 'Ancient'
            
            self.hp = self.hit_die[0] * r
            
            if dragon_size != 'Average':
                self.name = '{} {} {}'.format(age,dragon_size,self.name)
            else:
                self.name = '{} {}'.format(age,self.name)
    
        
        self.c_type = c_type
        if not type(c_type) == DragonType:
            self.hp, self.hit_die = rollHP(self.c_type.hit_die)
        self.intelligence = determineIntelligence(self.c_type.iq)
        self.xp = determineXP(self)
        

        
    def __str__(self):
        output = '{}HP {} {}XP'.format(self.hp, self.name, self.xp)
        return output
    
def determineIntelligence(iq):
    if iq == 'Non': intellect = 0
    elif iq == 'Animal': intellect = 1
    elif iq == 'Semi': intellect = randint(2,4)
    elif iq == 'Low': intellect = randint(5,7)
    elif iq == 'Average': intellect = randint(8,10)
    elif iq == 'Very': intellect = randint(11,12)
    elif iq == 'High': intellect = randint(13,14)
    elif iq == 'Exceptional': intellect = randint(15,16)
    elif iq == 'Genuis': intellect = randint(17,18)
    elif iq == 'Supra-Genius': intellect = randint(19,20)
    elif iq == 'Godlike': intellect = 21
    
    return intellect
    
