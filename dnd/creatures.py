def findCreatureType(name):
    name_tokens = name.lower().split()
    num_tokens = len(name_tokens)
    if num_tokens < 1:
        return None
    #Find initial candidates
    candidates = [c for c in creature_types
                    if name_tokens[0] in c.name.lower()] 
    
    #check if each word is within the creature's full name
    for token in name_tokens[1:]:
        candidates = [c for c in candidates
                        if token in c.name.lower()]
        
    if len(candidates) == 0:
        print('Creature type not defined.')
        return None
    elif len(candidates) > 1:
        #if there is an exact match, drop others
        if name.lower() in [c.name.lower() for c in candidates]:
            found_idx = [c.name.lower() 
                              for c in candidates].index(name.lower())
            found_creature = candidates[found_idx]
        else:
            print('Found the following monsters, be more specific:')
            for c in candidates:
                print(c.name)
            return None
    else:
        found_creature = candidates[0]

    return found_creature

def generateCreatures(name):
    type = findCreatureType(name)
    if not type:
        return None
    
    
    creature_list = []
    num = randint(type.num[0], type.num[1])
    for i in range(num):
        creature_list.append(Creature(type))
        
    return creature_list
    
