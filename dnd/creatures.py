def rollHP(hit_die):
    min_hd = hit_die[0]
    max_hd = hit_die[1]
    mod = hit_die[2]
    
    hd = (randint(min_hd,max_hd), mod)
    hp = roll(8, hd[0]) + mod
    hp = max(hp,1)
    
    return [hp, hd]

